    public void setBadConsequence(BadConsequence b){
        badConsequenceModel=b;
        text.setText(badConsequenceModel.getText());
        level.setText(Integer.toString(badConsequenceModel.getLevels()));
        death.setText(Boolean.toString(badConsequenceModel.getDeath()));
        nhidden.setText(Integer.toString(badConsequenceModel.getNHiddenTreasures()));
        nvisibles.setText(Integer.toString(badConsequenceModel.getNVisibleTreasures()));
        fillTreasurePanel(visible, badConsequenceModel.getSpecificVisibleTreasures());
        fillTreasurePanel(hidden, badConsequenceModel.getSpecificHiddenTreasures());
        repaint();
        
    }
    
    public void fillTreasurePanel (JPanel aPanel, ArrayList<TreasureKind> aList){
        aPanel.removeAll();
        JLabel etiqueta = new JLabel();
        String valores = new String();
        for (TreasureKind t : aList){
            valores = valores + t.toString() + ",";
        }
        etiqueta.setText(valores);
        aPanel.add(etiqueta);
        aPanel.repaint();
        aPanel.revalidate();
    }
    
